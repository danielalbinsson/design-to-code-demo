// Merge the DTCG token source for the default theme (Brand Alpha · light)
// into a single JSON file that external tools can consume via one URL.
//
// Mirrors the `brand-alpha.css` source order in packages/acme-tokens/build.mjs:
//   primitive/** + brand-alpha/** + semantic/**
// References (e.g. "{space.2}") are preserved — this is the token *source*,
// not a resolved snapshot.
//
// Usage: node scripts/build-tokens-json.mjs

import { readFile, writeFile, readdir } from 'node:fs/promises'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const tokensRoot = path.resolve(__dirname, '../packages/tokens')
const outFile = path.resolve(__dirname, '../tokens.dtcg.json')

// Source directories, in merge order. Later files win on conflicts.
const sourceDirs = ['primitive', 'brand-alpha', 'semantic']

const isObject = (v) => v && typeof v === 'object' && !Array.isArray(v)

function deepMerge(target, source) {
  for (const key of Object.keys(source)) {
    if (isObject(source[key]) && isObject(target[key])) {
      deepMerge(target[key], source[key])
    } else {
      target[key] = source[key]
    }
  }
  return target
}

async function jsonFilesIn(dir) {
  const abs = path.join(tokensRoot, dir)
  const entries = await readdir(abs, { withFileTypes: true, recursive: true })
  return entries
    .filter((e) => e.isFile() && e.name.endsWith('.json'))
    .map((e) => path.join(e.parentPath ?? abs, e.name))
    .sort()
}

const merged = {
  $description:
    'Acme Design System — DTCG token source, default theme (Brand Alpha · light). Merged from packages/tokens (primitive + brand-alpha + semantic). References preserved. Generated by scripts/build-tokens-json.mjs.',
}

for (const dir of sourceDirs) {
  for (const file of await jsonFilesIn(dir)) {
    const data = JSON.parse(await readFile(file, 'utf8'))
    deepMerge(merged, data)
  }
}

await writeFile(outFile, JSON.stringify(merged, null, 2) + '\n', 'utf8')
console.log(`Wrote ${path.relative(path.resolve(__dirname, '..'), outFile)}`)
